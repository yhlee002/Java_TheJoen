
public class VariableEx03 {
	public static void main(String[] args) {
		//정수형 10, 2000, 5000000, 777777777
		//위 숫자를 알맞은 자료형 변수를 선언하여 대입하세요
		int num1 = 10; //byte num1 = 10;
		int num2 = 2000; //short num2 = 2000;
		int num3 = 5000000;
		int num4 = 777777777;
		
		//실수 3.14, 450.999999를 실수형 변수에 넣으시오.
		double doubleNum1 =  3.14;
		double doubleNum2 = 450.999999;
		
		//문자 가, 97, a, A를 문자형 변수에 넣으시오
		char ch1 = '가';
		char ch2 = 97; // 'a' 출력
		char ch3 = 'a';
		char ch4 = 'A'; //대문자는 65~
		
		//위 삽입한 값을 출력문을 이용해 출력하세요.
		//System.out.println(출력할 값);
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		
		System.out.println(doubleNum1);
		System.out.println(doubleNum2);
		
		System.out.println(ch1);
		System.out.println(ch2);
		System.out.println(ch3);
		System.out.println(ch4);
	}
}
